<h2>Instructions to the authors and Referees</h2>

<p>Articles should be in the word range of 2000 to 5000 words. APA style of referencing should be used.</p>
<p>The articles can be submitted in English, Hindi or Punjabi. The articles in English should be typed in MS Word format with Times New Roman font size 14 and 1.5 line space. Hindi article should be typed in KrutiDev 10 font. In Punjabi Drchatarik font should be used.</p>
<p>Download Font KrutiDev Ver.10</p>
<p>Download Font DrChatrik Web</p>
<p>The articles should be submitted online at the link "Submit Article" provided on the website of the journal.</p>

<h3>Due process has to be followed while submitting the article.</h3>
<ul>
<li>The author should submit the details in particular columns along-with Title and abstract of the paper.</li>
<li>The main part of the article should not contain the name of the author. This is to ensure confidentiality during peer review process. The articles will be published after the recommendations of the referees.</li>
<li>Papers must be written solely by the candidate, in English, and may not have been submitted for publication elsewhere.</li>
<li>Plagiarism is strictly prohibited and shall lead to immediate rejection.</li>
<li>The decision regarding the status of publication can be known through email.</li>
<li>As per the decision of the Editorial Board, No Publication charges shall be levied</li>
</ul>
<h3>Paper Submission Process</h3>
<ul>
<li>Authors should send their papers using the "Submit Article" link on iq.cgcta.in. Article should be submitted in pdf form. All communication regarding acceptance or rejection shall be made through email.</li>
<li>For any queries, contact us using the "Contact Us" link on the website.</li>
</ul>

<h3>Guidelines for Referees</h3>
<p>You shall be sent the manuscript by the Chiefeditor of the journal. The article shall generally relate to your area of specialization. However, you may send the refusal to review in case the article/research paper is not related to your expertise. Any manuscript found to have any details of authors should not be reviewed and returned back to the Chief Editor.</p>
<ul>
<li>Due check should be made with regard to the references given by the author.</li>
<li>Due weightage should be given to interdisciplinary relevance of the papers.</li>
<li>The research article should be cohesive and engaging.</li>
<li>The articles which are substandard and do not provide any major contribution to the literature on social issues should not be recommended for publication.</li>
<li>Strict confidentiality of the contributor should be maintained.</li>
<li>The decision should contain any of the following choices<br> a) Accepted b) Rejected c) May be accepted with modifications</li>
<li>Every article shall be reviewed at least by two referees. The decision of the Chief Editor will be final in this regard.</li>
<li>Submission should also mandatorily include the author's view, in the conclusion, taking into considerations the aspects/contentions discussed throughout the manuscript.</li>
</ul>
<p>The reviewers are required to satisfy themselves regarding the originality, cohesiveness and relevance of the matter written in the research paper/article.</p>